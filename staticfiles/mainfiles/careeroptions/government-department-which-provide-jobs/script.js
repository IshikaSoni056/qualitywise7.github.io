  function displayDepartments() {
    let departmentHTML = "";
    for (const department in governmentDepartments) {
      departmentHTML += `<h2>${department}</h2>`;
      departmentHTML += `<ul>`;
      for (const branch in governmentDepartments[department]) {
        departmentHTML += `<li>${branch}: ${governmentDepartments[department][branch]}</li>`;
      }
      departmentHTML += `</ul>`;
    }
    departmentList.innerHTML = departmentHTML;
  }
  
